package com.DataTypes;

public class PrimitiveDataTypes {
    public static void main(String[] args){
        //Primitive DataTypes

        //integer types - byte, short,int, long
        byte  a=  100; // 1-byte
        short b = 2000; // 2-bytes
        int   c = 120000; // 4- bytes
        long  e = 67890678; // 8 -Bytes

        // Floating Points
        float d= 23.00f; //stores 6 decimal digits - 4bytes
        double f = 5678902.12d; //stores 16 digits after decimal numbers - 8bytes

        //characters
        char  g = 'A'; // 2 bytes -single letter
        // Boolean Types
        boolean h = true; // 1 bit - Yes/No OR True/False or on/off
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);

    }


}
