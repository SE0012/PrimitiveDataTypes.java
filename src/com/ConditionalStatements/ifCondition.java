package com.ConditionalStatements;

public class ifCondition {
    /*
  - "if" STATEMENT - "if" is used to specify a block of code to be executed.
  - Syntax:
    if(Condition)
    { Block }
  - if the condition is true then the block will be executed .
     */
    //CODE - even or odd numbers
    public static void main(String[] args) {
        int n1 = 10;
        if (n1 % 2 == 0) {
            System.out.println(n1);
        }

    }
}




